n = bin(10) #entero a binario
print (n)

n = hex(10) #entero a hexadecimal
print (n)

n = int("0b1010",2) #binario a entero, el 2 es la base 2
print (n)

n = int("0xa",16) #hexadecimal a entero, el 16 es la base 16
print (n)

